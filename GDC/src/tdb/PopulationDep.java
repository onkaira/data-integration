package tdb;

public class PopulationDep {
	private String idDep;
	private String depName;
	private float 	popMoyenne;
	private float natalite;
	
	public String getIdDep() {
		return idDep;
	}
	public void setIdDep(String idDep) {
		this.idDep = idDep;
	}
	public String getDepName() {
		return depName;
	}
	public void setDepName(String depName) {
		this.depName = depName;
	}
	public float getPopMoyenne() {
		return popMoyenne;
	}
	public void setPopMoyenne(float popMoyenne) {
		this.popMoyenne = popMoyenne;
	}
	public float getNatalite() {
		return natalite;
	}
	public void setNatalite(float natalite) {
		this.natalite = natalite;
	}
	
}
