package hbase;

public class SanteDep {
	private String idDep;
	private String depName;
	private int 	nbPharmacies;
	private int nbHopitaux;
	
	
	public String getIdDep() {
		return idDep;
	}
	public void setIdDep(String idDep) {
		this.idDep = idDep;
	}
	public String getDepName() {
		return depName;
	}
	public void setDepName(String depName) {
		this.depName = depName;
	}
	public int getNbPharmacies() {
		return nbPharmacies;
	}
	public void setNbPharmacies(int nbPharmacies) {
		this.nbPharmacies = nbPharmacies;
	}
	public int getNbHopitaux() {
		return nbHopitaux;
	}
	public void setNbHopitaux(int nbHopitaux) {
		this.nbHopitaux = nbHopitaux;
	}

	
	
	

}
